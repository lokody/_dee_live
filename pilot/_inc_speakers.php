					<h2>Prof. Yuriko Saito</h2>
					<p>is Professor Emerita of Philosophy at the Rhode Island School of Design, USA, and Editor of Contemporary Aesthetics. Her works on everyday aesthetics, Japanese aesthetics, and environmental aesthetics appeared in numerous journals, anthologies, and encyclopaedias, and she lectured widely on these issues. She published three monographs: <em>Everyday Aesthetics (Oxford, 2008), Aesthetics of the Familiar: Everyday Life and World-Making</em> (Oxford, 2017, awarded the outstanding monograph prize from the American Society for Aesthetics) and <em>Aesthetics of Care: Practice in Everyday Life</em> (Bloomsbury, 2022).</p>
