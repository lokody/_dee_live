				<div class="bio-container">
					<h2>Prof. Michalle Gal</h2>
					
					<p>is a professor of philosophy at the the Unit of History and Philosophy of Art and Design and the Interdisciplinary Design Graduate Program, Shenkar College. Gal is the author of <em>Visual Metaphors and Aesthetics: A Formalist Theory of Metaphor (2022) Aestheticism: Deep Formalism and the Emergence of Modernist Aesthetics (2015) </em>, and the co-author of the forthcoming <em>Introduction to Design Theory</em>.  (2023) She is the editor of the special issues <em>Art and Gesture </em> (2014, Paragrana, De Gruyter), <em>Visual Hybrids</em> (2023, Poetics Today) and <em>Design and its Relations</em> (2023, Journal of Comparative Literature and Aesthetics), and the author of numerous of essays. Gal’s current project is entitled “Visualism”: a new externalist, broad version of Formalism in aesthetics, which endorses the evolving “visual turn” in philosophy. It emphasizes the visual sphere as the correct context for addressing human nature, praxis, and theories of reality, challenges the dominant conceptualist-cognitivist school in philosophy and aesthetics, cognitive studies, and linguistics, and introduces to aesthetics a visualist line of research on design, art, ontology and the mind.
</p>

					

					
